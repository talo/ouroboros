    #[test]
    fn test_enum_int() {
        mod int {
            use ouroboros_proc_macro::*;
            use rex::Rex;

            #[derive(Rex, TypeInfo)]
            pub enum Foo {
                One = 1,
                Two = 2,
                Three = 3,
            }
        }

        mod plain {
            use ouroboros_proc_macro::*;
            use rex::Rex;

            #[derive(Rex, TypeInfo)]
            pub enum Foo {
                One,
                Two,
                Three,
            }
        }

        // Rex's ADTVariant doesn't keep track of the integer values associated with enum
        // variants. So when we convert back to the ouroboros type, we'll get the same result
        // as derive(TypeInfo) would produce if there were no integer values in the enum.
        assert_eq!(
            *ouroboros_to_rex(&int::Foo::t(), &Default::default()).unwrap(),
            int::Foo::to_type()
        );
        assert_eq!(
            rex_to_ouroboros(&int::Foo::to_type()).unwrap(),
            plain::Foo::t()
        );
    }

