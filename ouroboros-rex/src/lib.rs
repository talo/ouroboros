    #[test]
    fn test_numeric_roundtrip() {
        // Rex only supports 64-bit numeric types (Uint, Int, Float). Converting from ouroboros
        // types to rex again will therefore result in only the 64-bit variants being present.
        mod different_sizes {
            use ouroboros_proc_macro::*;
            use rex::Rex;

            #[derive(Rex, TypeInfo)]
            pub struct Foo {
                pub u1: u8,
                pub u2: u16,
                pub u3: u32,
                pub u4: u64,
                pub i1: i8,
                pub i2: i16,
                pub i3: i32,
                pub i4: i64,
                pub f1: f32,
                pub f2: f64,
            }
        }

        mod all_64 {
            use ouroboros_proc_macro::*;
            use rex::Rex;

            #[derive(Rex, TypeInfo)]
            pub struct Foo {
                pub u1: u64,
                pub u2: u64,
                pub u3: u64,
                pub u4: u64,
                pub i1: i64,
                pub i2: i64,
                pub i3: i64,
                pub i4: i64,
                pub f1: f64,
                pub f2: f64,
            }
        }

        assert_eq!(
            rex_to_ouroboros(
                &ouroboros_to_rex(&different_sizes::Foo::t(), &Default::default()).unwrap()
            )
            .unwrap(),
            all_64::Foo::t()
        );

        assert_eq!(
            *ouroboros_to_rex(&different_sizes::Foo::t(), &Default::default()).unwrap(),
            all_64::Foo::to_type()
        );
    }

